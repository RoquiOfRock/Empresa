    private void BtnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSiguienteActionPerformed
        if(txtidP.getText().isEmpty()||txtAm.getText().isEmpty()||txtD.getText().isEmpty()){
        JOptionPane.showMessageDialog(null, "Falta informacion Valida", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            if(BtnRLocal.isSelected()){
                dispose();
                setVisible(false);
                FrameLocales VLocales = new FrameLocales();
                VLocales.setVisible(true);
            }else{
                if(BtnRVivienda.isSelected()){
                    dispose();
                    setVisible(false);
                    FrameVivienda VVivienda = new FrameVivienda();
                    VVivienda.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null, "No seleciono el tipo de Inmueble", "Error De Selecion", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        
    }//GEN-LAST:event_BtnSiguienteActionPerformed

